package com.inventario.pauny.dtos;

import java.math.BigDecimal;
import java.util.ArrayList;
import java.util.Date;
import java.util.List;
import lombok.AllArgsConstructor;
import lombok.Data;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;
import lombok.ToString;

/**
 *
 * @author Exequiel
 */
@AllArgsConstructor
@NoArgsConstructor
public class OrdenDTO {

    private Long id;
    private String nroOrden;
    private String tipoOrden;
    private ClienteDTO cliente;
    private SucursalDTO sucursal;
    private List<OrdenDetalleDTO> ordenDetalle;
    private String metodoPago;
    private BigDecimal total;
    private Date fechaRegistro;

    public Long getId() {
        return id;
    }

    public Date getFechaRegistro() {
        return fechaRegistro;
    }

    public void setFechaRegistro(Date fechaRegistro) {
        this.fechaRegistro = fechaRegistro;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getNroOrden() {
        return nroOrden;
    }

    public void setNroOrden(String nroOrden) {
        this.nroOrden = nroOrden;
    }

    public String getTipoOrden() {
        return tipoOrden;
    }

    public void setTipoOrden(String tipoOrden) {
        this.tipoOrden = tipoOrden;
    }

    public ClienteDTO getCliente() {
        return cliente;
    }

    public void setCliente(ClienteDTO cliente) {
        this.cliente = cliente;
    }

    public SucursalDTO getSucursal() {
        return sucursal;
    }

    public void setSucursal(SucursalDTO sucursal) {
        this.sucursal = sucursal;
    }

    public List<OrdenDetalleDTO> getOrdenDetalle() {
        return ordenDetalle;
    }

    public void setOrdenDetalle(List<OrdenDetalleDTO> ordenDetalle) {
        this.ordenDetalle = ordenDetalle;
    }

    public String getMetodoPago() {
        return metodoPago;
    }

    public void setMetodoPago(String metodoPago) {
        this.metodoPago = metodoPago;
    }

    public BigDecimal getTotal() {
        return total;
    }

    public void setTotal(BigDecimal total) {
        this.total = total;
    }

}
